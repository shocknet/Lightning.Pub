    decodeOutboundInvoice(invoice: string): number {
        if (!invoice.startsWith('lnbcrtmockout')) {
            throw new Error("invalid mock invoice provided for payment")
        }
        const amt = invoice.substring('lnbcrtmockout'.length).split("__")[0]
        if (isNaN(+amt)) {
            throw new Error("invalid mock invoice provided for payment")
        }
        return +amt
    }

