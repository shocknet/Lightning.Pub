import NewHttpClient from './autogenerated/ts/http_client.js'
const CreateHttpClient = (url: string, pubSecret: string) => {
    return NewHttpClient({
        baseUrl: url,
        retrieveGuestAuth: async () => { return "" },
        retrieveWalletAuth: async () => { throw new Error("wallet routes not enabled") },
        retrievePubAuth: async () => { return pubSecret },
        encryptCallback: async () => { throw new Error("encryption not enabled") },
        decryptCallback: async () => { throw new Error("encryption not enabled") },
        deviceId: "",
    })
}
export default CreateHttpClient

export type BridgeHttpClient = ReturnType<typeof CreateHttpClient>
