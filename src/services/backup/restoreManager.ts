function failureMessage(source: wizardTypes.RestoreRequest_source_type, shard: BackupTableId): string {
    const name = backupTableFilename(shard)
    switch (source) {
        case wizardTypes.RestoreRequest_source_type.CLOUD:
            return `No backup found for this seed on the managed service (missing ${name}). Were backups enabled on the original instance? Did this seed ever run Lightning.Pub?`
        case wizardTypes.RestoreRequest_source_type.FTP_HOST:
            return `Could not connect or ${name} not found — verify host, credentials, and path.`
        case wizardTypes.RestoreRequest_source_type.LOCAL_PATH:
            return `${name} not found or path is not a readable directory — expected a folder of per-table *.enc shards from backup.`
    }
}


export const parseRestoreFlags = (flags: Record<string, string>): wizardTypes.RestoreRequest => {
    const phrase = flags['phrase']
    if (!phrase) {
        throw new Error('Error: --phrase is required')
    }
    const source = flags['source'] as RestoreSource
    if (!source) {
        throw new Error('Error: --source is required (cloud|ftp|local)')
    }

    if (!validRestoreSources.includes(source)) {
        throw new Error(`Error: --source must be one of: ${validRestoreSources.join(', ')}`)
    }
    const credsOverr = !!flags['ftp-user'] && !!flags['ftp-pass']
    const creds: wizardTypes.FtpCreds | undefined = credsOverr ? {
        user: flags['ftp-user'],
        pass: flags['ftp-pass'],
    } : undefined
    const relay = flags['relay'] ? flags['relay'] : undefined

    if (source === 'ftp') {
        const sftpHost = flags['ftp-host']
        if (!sftpHost) {
            throw new Error('Error: --ftp-host is required when source=ftp')
        }
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.FTP_HOST,
                ftp_host: sftpHost,
            }, creds_override: creds, relay

        }
    } else if (source === 'local') {
        const localPath = flags['local-path']
        if (!localPath) {
            throw new Error('Error: --local-path is required when source=local')
        }
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.LOCAL_PATH,
                local_path: localPath,
            }, creds_override: creds, relay
        }
    } else {
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.CLOUD,
                cloud: {}
            }, creds_override: creds, relay
        }
    }
}