export type RestoreResult = {
    success: boolean
    error?: string
    tablesRestored?: number
}
const DeadLineMetadata = (deadline = 20 * 1000) => ({ deadline: Date.now() + deadline })
const SCB_BACKUP_KIND = 30078
const SCB_BACKUP_D_TAG = 'Lightning.Pub/backup/scb'
const relayFetchTimeoutMs = 12_000

export class RestoreManager {
    storage: Storage
    settings: SettingsManager
    log = getLogger({ component: 'restoreManager' })
    unlocker: Unlocker
    constructor(storage: Storage, settings: SettingsManager, unlocker: Unlocker) {
        this.storage = storage
        this.settings = settings
        this.unlocker = unlocker
    }
    async RestoreFromSource(req: wizardTypes.RestoreRequest): Promise<wizardTypes.RestoreResponse> {
        try {
            this.log('RestoreFromSource request received', req.source.type)
            const clean = await this.storage.IsDbClean()
            if (!clean) {
                return {
                    tables_restored: 0,
                    success: false,
                    error: 'Database is not empty. Restore can only run against a freshly initialized database.',
                }
            }
            if (!req.phrase || req.phrase.trim() === '') {
                return {
                    tables_restored: 0,
                    success: false,
                    error: 'No phrase provided to restore',
                }
            }
            this.log("deriving backup keys")
            const keys = await deriveBackupKeys(req.phrase, LATEST_DERIVATION_VERSION)

            const buffers = await this.fetchSegmentsData(req, keys)

            const { backupData } = this.decodeSegmentsData(buffers, keys)
            const addressesCount = backupData.indexes.find(i => i.addressesCount !== 0)?.addressesCount ?? 0
            this.log("addresses count: " + addressesCount)
            const recoveryWindow = Math.max(1000, addressesCount * 10)

            const apps = backupData.applications
            if (apps.length === 0) {
                throw new Error('No applications found in backup, cannot restore')
            }
            const existingWalletApp = selectDefaultApp(apps, this.settings.getSettings().serviceSettings.defaultAppName)
            if (!existingWalletApp) {
                throw new Error('No default wallet app found in backup, cannot restore')
            }
            const pubkey = existingWalletApp.nostr_public_key
            const privateKey = existingWalletApp.nostr_private_key
            if (!pubkey || !privateKey) {
                throw new Error('Default wallet app has no nostr keys, cannot restore')
            }

            const scbData = await this.retrieveSCB(req, pubkey)
            const decryptedScb = await this.unlocker.DecryptScbEvent(scbData, { nostr_private_key: privateKey, nostr_public_key: pubkey })
            if (!decryptedScb) {
                throw new Error('Failed to decrypt SCB data')
            }

            this.log("All data needed for restore is available, starting restore process")

            const tablesRestored = await this.storage.StartTransaction(async tx => {
                this.log("importing dialtone")
                const entriesRestored = await this.importDialtone(backupData, tx)
                await this.UnlockLnd(req, recoveryWindow)
                return entriesRestored
            })

            try {
                await this.restoreScb(decryptedScb)
            } catch (err: any) {
                this.log("Skipping SCB restore: " + err.message || err)
            }
            this.log("RestoreFromSource completed")
            return {
                tables_restored: tablesRestored,
                success: true,
                error: '',
            }
        } catch (err: any) {
            this.log('RestoreFromSource failed', err.message || err)
            return {
                tables_restored: 0,
                success: false,
                error: err.message || 'restore failed',
            }
        }
    }

    async UnlockLnd(req: wizardTypes.RestoreRequest, recoveryWindow: number) {
        this.log("unlocking LND wallet with window: " + recoveryWindow)
        const phrase = req.phrase.split(' ')
        const unlockResult = await this.unlocker.Unlock({ seed: phrase, recoveryWindow })
        if (unlockResult !== 'created') {
            throw new Error('Failed to create LND wallet')
        }
    }

    async fetchSegmentsData(req: wizardTypes.RestoreRequest, keys: DerivedKeys) {
        this.log("fetching segments data")
        const buffers = new Map<BackupTableId, Buffer>()
        for (const id of BACKUP_RESTORE_ORDER) {
            const name = backupTableFilename(id)
            const chunk = await fetchFile(this.log, keys, req, name)
            if (!chunk.found) {
                this.log("buffer not found: " + name)
                continue
            }
            buffers.set(id, chunk.data)
        }
        return buffers
    }

    decodeSegmentsData(buffers: Map<BackupTableId, Buffer>, keys: DerivedKeys) {
        this.log("decoding segments data")
        const readRows = <T>(id: BackupTableId, decodeRow: (u: Uint8Array) => T): T[] => {
            const buffer = buffers.get(id)
            if (!buffer) {
                this.log("buffer not found: " + id)
                return []
            }
            //this.log("decrypting table: " + id + " length: " + buffer.length)
            const rows = decryptTableRows(buffer, keys.encKey).map(decodeRow)
            //this.log("rows found: " + id + " length: " + rows.length)
            return rows
        }

        const backupData: BackupData = {
            indexes: readRows('indexes', decodeIndexesRow),
            balances: readRows('user_balances', decodeBalanceRow),
            trackedProviders: readRows('tracked_providers', decodeTrackedProviderRow),
            applications: readRows('applications', decodeApplicationRow),
            applicationUsers: readRows('application_users', decodeApplicationUserRow),
            adminSettings: readRows('admin_settings', decodeAdminSettingRow),
            appUserDevices: readRows('app_user_devices', decodeAppUserDeviceRow),
            userOffers: readRows('user_offers', decodeUserOfferRow),
            products: readRows('products', decodeProductRow),
            managementGrants: readRows('management_grants', decodeManagementGrantRow),
            debitAccesses: readRows('debit_accesses', decodeDebitAccessRow),
            inviteTokens: readRows('invite_tokens', decodeInviteTokenRow),
        }

        this.log("segments data decrypted, importing dialtone")
        this.log("backupData Data: " + Object.entries(backupData).map(([key, value]) => key + ": " + value.length).join(", "))
        return { backupData }
    }

    async importDialtone(backupData: BackupData, tx: string) {
        let entriesRestored = 0
        const allowPartial = false
        const { balances, trackedProviders, applications,
            applicationUsers, adminSettings, appUserDevices, userOffers,
            products, managementGrants, debitAccesses, inviteTokens } = backupData

        await this.restore("balances", balances.length,
            () => this.storage.userStorage.RestoreBalances(balances, tx), allowPartial)
        entriesRestored += balances.length
        await this.restore("trackedProviders", trackedProviders.length,
            () => this.storage.liquidityStorage.RestoreTrackedProviders(trackedProviders, tx), allowPartial)
        entriesRestored += trackedProviders.length

        await this.restore("applications", applications.length,
            () => this.storage.applicationStorage.RestoreApplications(applications, tx), allowPartial)
        entriesRestored += applications.length
        await this.restore("applicationUsers", applicationUsers.length,
            () => this.storage.applicationStorage.RestoreApplicationUsers(applicationUsers, tx), allowPartial)
        entriesRestored += applicationUsers.length
        await this.restore("adminSettings", adminSettings.length,
            () => this.storage.settingsStorage.RestoreSettings(adminSettings, tx), allowPartial)
        entriesRestored += adminSettings.length
        await this.restore("appUserDevices", appUserDevices.length,
            () => this.storage.applicationStorage.RestoreAppUserDevices(appUserDevices, tx), allowPartial)
        entriesRestored += appUserDevices.length
        await this.restore("userOffers", userOffers.length,
            () => this.storage.offerStorage.RestoreUserOffers(userOffers, tx), allowPartial)
        entriesRestored += userOffers.length
        await this.restore("products", products.length,
            () => this.storage.productStorage.RestoreProducts(products, tx), allowPartial)
        entriesRestored += products.length
        await this.restore("managementGrants", managementGrants.length,
            () => this.storage.managementStorage.RestoreManagementGrants(managementGrants, tx), allowPartial)
        entriesRestored += managementGrants.length
        await this.restore("debitAccesses", debitAccesses.length,
            () => this.storage.debitStorage.RestoreDebitAccesses(debitAccesses, tx), allowPartial)
        entriesRestored += debitAccesses.length
        await this.restore("inviteTokens", inviteTokens.length,
            () => this.storage.applicationStorage.RestoreInviteTokens(inviteTokens, tx), allowPartial)
        entriesRestored += inviteTokens.length
        return entriesRestored
    }

    private restore = async (name: string, length: number, f: () => Promise<number>, allowPartial = false) => {
        const r = await f()
        this.log("restored", r, "of", length, name)
        if (!allowPartial && r !== length) throw new Error("failed to restore all " + name)
    }

    private retrieveSCB = async (req: wizardTypes.RestoreRequest, pubkey: string) => {
        const relay = this.getRestoreRelay(req)
        if (!relay) {
            throw new Error('SCB restore skipped: no relay configured')
        }
        const scbData = await this._fetchScbDataFromRelay(relay, pubkey)
        if (!scbData) {
            throw new Error('SCB restore skipped: no SCB backup event found on relay')
        }
        return scbData
    }

    private restoreScb = async (scb: Buffer) => {
        this.log("restoring SCB")
        await this.unlocker.ApplyScb(scb)
    }

    private getRestoreRelay = (req: wizardTypes.RestoreRequest): string | null => {
        if (req.relay && req.relay.trim()) {
            return req.relay.trim()
        }
        const relays = this.settings.getSettings().nostrRelaySettings.relays
        return relays[0] || null
    }

    _fetchScbDataFromRelay = async (relayUrl: string, pubkey: string): Promise<string | null> => {
        const relay = await Relay.connect(relayUrl)
        try {
            const events: NostrEvent[] = []
            await new Promise<void>((resolve) => {
                const sub = relay.subscribe([{
                    kinds: [SCB_BACKUP_KIND],
                    authors: [pubkey],
                    '#d': [SCB_BACKUP_D_TAG],
                }], {
                    onevent: (e) => events.push(e),
                    oneose: () => {
                        sub.close()
                        resolve()
                    }
                })
                setTimeout(() => {
                    sub.close()
                    resolve()
                }, relayFetchTimeoutMs)
            })
            if (events.length === 0) {
                return null
            }
            const latest = events.sort((a, b) => (b.created_at || 0) - (a.created_at || 0))[0]
            return latest.content
        } finally {
            relay.close()
        }
    }
}

const fetchFile = async (log: PubLogger, keys: DerivedKeys, opts: wizardTypes.RestoreRequest, filename: string): Promise<SFTPFile> => {
    log("fetching file: " + filename, "source: " + opts.source.type)
    switch (opts.source.type) {
        case wizardTypes.RestoreRequest_source_type.CLOUD:
            const couldConf = cloudSftpConfig(keys.sftpUser, keys.sftpPass)
            return sftpDownload(couldConf, filename)
        case wizardTypes.RestoreRequest_source_type.FTP_HOST:
            if (!opts.source.ftp_host) throw new Error('--ftp-host is required for source=ftp')
            const sftpConf: SftpConfig = {
                host: opts.source.ftp_host,
                username: opts.creds_override?.user ?? keys.sftpUser,
                password: opts.creds_override?.pass ?? keys.sftpPass,
            }
            return sftpDownload(sftpConf, filename)

        case wizardTypes.RestoreRequest_source_type.LOCAL_PATH: {
            const dir = opts.source.local_path
            if (!dir) throw new Error('Local path is required for source=local')
            if (!fs.existsSync(dir)) {
                return { found: false }
            }
            const stat = fs.statSync(dir)
            if (!stat.isDirectory()) {
                return { found: false }
            }
            const filePath = path.join(dir, filename)
            if (!fs.existsSync(filePath) || !fs.statSync(filePath).isFile()) {
                return { found: false }
            }
            return { found: true, data: fs.readFileSync(filePath) }
        }

        default:
            throw new Error(`Unknown restore source: ${opts.source}`)
    }
}

function failureMessage(source: wizardTypes.RestoreRequest_source_type, shard: BackupTableId): string {
    const name = backupTableFilename(shard)
    switch (source) {
        case wizardTypes.RestoreRequest_source_type.CLOUD:
            return `No backup found for this seed on the managed service (missing ${name}). Were backups enabled on the original instance? Did this seed ever run Lightning.Pub?`
        case wizardTypes.RestoreRequest_source_type.FTP_HOST:
            return `Could not connect or ${name} not found — verify host, credentials, and path.`
        case wizardTypes.RestoreRequest_source_type.LOCAL_PATH:
            return `${name} not found or path is not a readable directory — expected a folder of per-table *.enc shards from backup.`
    }
}


export const parseRestoreFlags = (flags: Record<string, string>): wizardTypes.RestoreRequest => {
    const phrase = flags['phrase']
    if (!phrase) {
        throw new Error('Error: --phrase is required')
    }
    const source = flags['source'] as RestoreSource
    if (!source) {
        throw new Error('Error: --source is required (cloud|ftp|local)')
    }

    if (!validRestoreSources.includes(source)) {
        throw new Error(`Error: --source must be one of: ${validRestoreSources.join(', ')}`)
    }
    const credsOverr = !!flags['ftp-user'] && !!flags['ftp-pass']
    const creds: wizardTypes.FtpCreds | undefined = credsOverr ? {
        user: flags['ftp-user'],
        pass: flags['ftp-pass'],
    } : undefined
    const relay = flags['relay'] ? flags['relay'] : undefined

    if (source === 'ftp') {
        const sftpHost = flags['ftp-host']
        if (!sftpHost) {
            throw new Error('Error: --ftp-host is required when source=ftp')
        }
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.FTP_HOST,
                ftp_host: sftpHost,
            }, creds_override: creds, relay,
        }
    } else if (source === 'local') {
        const localPath = flags['local-path']
        if (!localPath) {
            throw new Error('Error: --local-path is required when source=local')
        }
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.LOCAL_PATH,
                local_path: localPath,
            }, creds_override: creds, relay,
        }
    } else {
        return {
            phrase, source: {
                type: wizardTypes.RestoreRequest_source_type.CLOUD,
                cloud: {}
            }, creds_override: creds, relay,
        }
    }
}