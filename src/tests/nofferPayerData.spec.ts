const testDeleteUserOffer = async (T: TestBase) => {
    T.d("starting testDeleteUserOffer")
    const ctx = userContext(T, T.user2)
    const { offer_id } = await T.main.offerManager.AddUserOffer(ctx, offerCreate({
        label: "crud delete test",
        payer_data: ["order_id"],
    }))

    await T.main.offerManager.DeleteUserOffer(ctx, { offer_id })

    let threw = false
    try {
        await T.main.offerManager.GetUserOffer(ctx, { offer_id })
    } catch (e: any) {
        threw = true
        T.expect(e.message).to.equal("Offer not found")
    }
    T.expect(threw).to.equal(true)
    T.d("DeleteUserOffer removed offer from GetUserOffer")
}

const testGetUserOfferInvoicesStoresPayerData = async (T: TestBase) => {
    T.d("starting testGetUserOfferInvoicesStoresPayerData")
    const ctx = userContext(T, T.user2)
    const { offer_id } = await T.main.offerManager.AddUserOffer(ctx, offerCreate({
        label: "invoice payer data test",
        payer_data: ["order_id"],
    }))

    const offerReq: NofferData = {
        offer: offer_id,
        payer_data: { order_id: "inv-456" },
    }
    const result = await T.main.offerManager.getNofferInvoice(offerReq, T.app.appId)
    T.expect(result.success).to.equal(true)

    const { invoices } = await T.main.offerManager.GetUserOfferInvoices(ctx, {
        offer_id,
        include_unpaid: true,
    })
    T.expect(invoices.length).to.be.at.least(1)
    const latest = invoices.find(i => i.data.order_id === "inv-456")
    T.expect(latest).to.not.equal(undefined)
    if (latest) {
        T.expect(latest.offer_id).to.equal(offer_id)
        T.expect(latest.data).to.deep.equal({ order_id: "inv-456" })
    }
    T.d("GetUserOfferInvoices returns payer_data stored on invoice")
}

