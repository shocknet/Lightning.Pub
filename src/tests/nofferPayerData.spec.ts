import { runSanityCheck, TestBase } from "./testBase.js"

export const ignore = false
export const dev = false

const userContext = (T: TestBase, user: TestBase["user2"]): Types.UserContext => ({
    app_id: T.app.appId,
    app_user_id: user.appUserIdentifier,
    user_id: user.userId,
})

const offerCreate = (
    overrides: Partial<Types.OfferCreateRequest> & Pick<Types.OfferCreateRequest, "label">
): Types.OfferCreateRequest => ({
    payer_data: [],
    price_sats: 1000,
    callback_url: "",
    blind: false,
    rejectUnauthorized: true,
    token: "",
    ...overrides,
})

const mockNostrEvent = (T: TestBase, offerReq: NofferData): NostrEvent => ({
    id: "test-event-id",
    pub: "test-requester-pub",
    content: JSON.stringify(offerReq),
    appId: T.app.appId,
    startAtNano: "0",
    startAtMs: Date.now(),
    kind: 21001,
})

const testFixedPriceOfferWithPayerData = async (T: TestBase) => {
    T.d("starting testFixedPriceOfferWithPayerData")
    const ctx = userContext(T, T.user2)
    const { offer_id } = await T.main.offerManager.AddUserOffer(ctx, offerCreate({
        payer_data: ["order_id"],
        label: "fixed offer with payer data",
    }))

    const offerReq: NofferData = {
        offer: offer_id,
        payer_data: { order_id: "abc123" },
    }
    const result = await T.main.offerManager.getNofferInvoice(offerReq, T.app.appId)

    T.expect(result.success).to.equal(true)
    if (result.success) {
        T.expect(result.invoice).to.startWith("lnbcrt")
    }
    T.d("fixed-price noffer with payer_data returned bolt11 invoice")
}

