const userContext = (T: TestBase, user: TestBase["user2"]): Types.UserContext => ({
    app_id: T.app.appId,
    app_user_id: user.appUserIdentifier,
    user_id: user.userId,
})

const offerCreate = (
    overrides: Partial<Types.OfferCreateRequest> & Pick<Types.OfferCreateRequest, "label">
): Types.OfferCreateRequest => ({
    payer_data: [],
    price_sats: 1000,
    callback_url: "",
    blind: false,
    rejectUnauthorized: true,
    token: "",
    ...overrides,
})

const expectNofferFail = (
    T: TestBase,
    result: Awaited<ReturnType<TestBase["main"]["offerManager"]["getNofferInvoice"]>>,
    code: number,
    error: string,
    opts?: { payer_data?: string[] },
) => {
    T.expect(result.success).to.equal(false)
    if (!result.success) {
        T.expect(result.code).to.equal(code)
        T.expect(result.error).to.equal(error)
        if (opts?.payer_data) {
            T.expect(result.payer_data).to.deep.equal(opts.payer_data)
        }
    }
}

const mockNostrEvent = (T: TestBase, offerReq: NofferData): NostrEvent => ({
    id: "test-event-id",
    pub: "test-requester-pub",
    content: JSON.stringify(offerReq),
    appId: T.app.appId,
    startAtNano: "0",
    startAtMs: Date.now(),
    kind: 21001,
})

