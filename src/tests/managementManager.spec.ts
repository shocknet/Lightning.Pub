const testNmanageCreateWithoutGrantLeavesNoOffer = async (T: TestBase) => {
    T.d("starting testNmanageCreateWithoutGrantLeavesNoOffer")
    const unknownManager = "e".repeat(64)
    const before = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, unknownManager)
    const req: NmanageCreateOffer = {
        resource: "offer",
        action: "create",
        pointer: T.user2.appUserIdentifier,
        offer: { fields: offerFields({ label: "unauthorized create" }) },
    }
    const app = await T.main.storage.applicationStorage.GetApplication(T.app.appId)
    const appUser = await T.main.storage.applicationStorage.GetApplicationUser(app, T.user2.appUserIdentifier)
    await T.main.storage.applicationStorage.AddNPubToApplicationUser(appUser.serial_id, USER_NPUB)
    await T.main.managementManager.handleRequest(req, mockNostrEvent(T, unknownManager))
    const after = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, unknownManager)
    T.expect(after).to.deep.equal(before)
    T.d("handleRequest create without grant did not persist offer")
}

const testAuthorizeManageProcessesAwaitingCreate = async (T: TestBase) => {
    T.d("starting testAuthorizeManageProcessesAwaitingCreate")
    const pendingManager = "f".repeat(64)
    const app = await T.main.storage.applicationStorage.GetApplication(T.app.appId)
    const appUser = await T.main.storage.applicationStorage.GetApplicationUser(app, T.user2.appUserIdentifier)
    await T.main.storage.applicationStorage.AddNPubToApplicationUser(appUser.serial_id, USER_NPUB)
    const req: NmanageCreateOffer = {
        resource: "offer",
        action: "create",
        pointer: T.user2.appUserIdentifier,
        offer: { fields: offerFields({ label: "awaiting create" }) },
    }
    await T.main.managementManager.handleRequest(req, mockNostrEvent(T, pendingManager, "awaiting-create-event"))
    let offers = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, pendingManager)
    T.expect(offers).to.have.length(0)
    const ctx = userContext(T, T.user2)
    await T.main.managementManager.AuthorizeManage(ctx, {
        authorize_npub: pendingManager,
        ban: false,
        request_id: "awaiting-create-event",
    })
    offers = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, pendingManager)
    T.expect(offers).to.have.length(1)
    T.expect(offers[0].label).to.equal("awaiting create")
    T.d("AuthorizeManage processed awaiting nmanage create request")
}

