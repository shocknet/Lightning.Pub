const expectNmanageError = (T: TestBase, err: NmanageFailure, code: number, error: string, field?: string) => {
    T.expect(err.res).to.equal("GFY")
    T.expect(err.code).to.equal(code)
    T.expect(err.error).to.equal(error)
    if (field !== undefined) {
        T.expect(err.field).to.equal(field)
    }
}

const withCapturedNostrSends = async <R>(
    storage: Storage,
    fn: (getLastNmanageError: () => NmanageFailure) => Promise<R>,
): Promise<R> => {
    const sent: SendData[] = []
    const sender = storage.NostrSender()
    const originalSend = sender.Send.bind(sender)
    sender.Send = (_initiator, data) => { sent.push(data) }
    const getLastNmanageError = (): NmanageFailure => {
        const eventSends = sent.filter((s): s is Extract<SendData, { type: "event" }> => s.type === "event")
        if (eventSends.length === 0) {
            throw new Error("expected nostr error event to be sent")
        }
        const last = eventSends[eventSends.length - 1]
        return JSON.parse(last.event.content) as NmanageFailure
    }
    try {
        return await fn(getLastNmanageError)
    } finally {
        sender.Send = originalSend
    }
}

const authorizeManager = async (T: TestBase, npub: string) => {
    const ctx = userContext(T, T.user2)
    const grant = await T.main.managementManager.AuthorizeManage(ctx, {
        authorize_npub: npub,
        ban: false,
    })
    T.expect(grant.authorized).to.equal(true)
    T.expect(grant.npub).to.equal(npub)
    return grant
}

