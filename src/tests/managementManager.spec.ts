const testNmanageUnauthorizedOfferUpdateRejected = async (T: TestBase) => {
    T.d("starting testNmanageUnauthorizedOfferUpdateRejected")
    const owner = managerPub(9)
    const intruder = managerPub(10)
    await authorizeManager(T, owner)
    await authorizeManager(T, intruder)
    const createReq: NmanageCreateOffer = {
        resource: "offer",
        action: "create",
        pointer: T.user2.appUserIdentifier,
        offer: { fields: offerFields({ label: "owner offer" }) },
    }
    await T.main.managementManager.handleRequest(createReq, mockNostrEvent(T, owner))
    const offers = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, owner)
    const offerId = offers[0].offer_id
    const updateReq: NmanageUpdateOffer = {
        resource: "offer",
        action: "update",
        offer: {
            id: offerId,
            fields: offerFields({ label: "intruder update" }),
        },
    }
    await withCapturedNostrSends(T.main.storage, async getLastNmanageError => {
        await T.main.managementManager.handleRequest(updateReq, mockNostrEvent(T, intruder))
        expectNmanageError(T, getLastNmanageError(), 1, "Request Denied: App not authorized to update offer")
    })
    const unchanged = await T.main.storage.offerStorage.GetOffer(offerId)
    T.expect(unchanged!.label).to.equal("owner offer")
    T.d("handleRequest update from non-owner manager returned error")
}

