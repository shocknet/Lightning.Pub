import { NmanageCreateOffer, NmanageDeleteOffer, NmanageGetOffer, NmanageListOffers, NmanageUpdateOffer, OfferFields } from "@shocknet/clink-sdk"
import * as Types from "../../proto/autogenerated/ts/types.js"
import { NostrEvent } from "../services/nostr/nostrPool.js"
import { runSanityCheck, TestBase } from "./testBase.js"

export const ignore = false
export const dev = false

const MANAGER_PUB = "c".repeat(64)
const USER_NPUB = "d".repeat(64)

const userContext = (T: TestBase, user: TestBase["user2"]): Types.UserContext => ({
    app_id: T.app.appId,
    app_user_id: user.appUserIdentifier,
    user_id: user.userId,
})

const mockNostrEvent = (T: TestBase, pub: string, id?: string): NostrEvent => ({
    id: id || `manage-event-${Date.now()}`,
    pub,
    content: "",
    appId: T.app.appId,
    startAtNano: "0",
    startAtMs: Date.now(),
    kind: 21003,
})

const offerFields = (overrides: Partial<OfferFields> = {}): OfferFields => ({
    label: "managed offer",
    price_sats: 500,
    callback_url: "",
    payer_data: [],
    ...overrides,
})

const authorizeManager = async (T: TestBase) => {
    const ctx = userContext(T, T.user2)
    const grant = await T.main.managementManager.AuthorizeManage(ctx, {
        authorize_npub: MANAGER_PUB,
        ban: false,
    })
    T.expect(grant.authorized).to.equal(true)
    T.expect(grant.npub).to.equal(MANAGER_PUB)
    return grant
}

const testGetManageAuthorizationsEmpty = async (T: TestBase) => {
    T.d("starting testGetManageAuthorizationsEmpty")
    const ctx = userContext(T, T.user2)
    const { manages } = await T.main.managementManager.GetManageAuthorizations(ctx)
    const existing = manages.filter(m => m.npub === MANAGER_PUB)
    T.expect(existing).to.deep.equal([])
    T.d("GetManageAuthorizations returns no entries for unknown manager")
}

const testAuthorizeManageGrant = async (T: TestBase) => {
    T.d("starting testAuthorizeManageGrant")
    const grant = await authorizeManager(T)
    const ctx = userContext(T, T.user2)
    const { manages } = await T.main.managementManager.GetManageAuthorizations(ctx)
    const stored = manages.find(m => m.npub === MANAGER_PUB)
    T.expect(stored).to.not.equal(undefined)
    T.expect(stored!.manage_id).to.equal(grant.manage_id)
    T.expect(stored!.authorized).to.equal(true)
    T.d("AuthorizeManage created management grant")
}

const testResetManage = async (T: TestBase) => {
    T.d("starting testResetManage")
    const ctx = userContext(T, T.user2)
    await T.main.managementManager.ResetManage(ctx, { npub: MANAGER_PUB })
    const { manages } = await T.main.managementManager.GetManageAuthorizations(ctx)
    const stored = manages.find(m => m.npub === MANAGER_PUB)
    T.expect(stored).to.equal(undefined)
    T.d("ResetManage removed management grant")
}

const testNmanageCreateOffer = async (T: TestBase) => {
    T.d("starting testNmanageCreateOffer")
    await authorizeManager(T)
    const req: NmanageCreateOffer = {
        resource: "offer",
        action: "create",
        pointer: T.user2.appUserIdentifier,
        offer: { fields: offerFields({ label: "nmanage create", payer_data: ["order_id"] }) },
    }
    await T.main.managementManager.handleRequest(req, mockNostrEvent(T, MANAGER_PUB))
    const offers = await T.main.storage.offerStorage.getManagedUserOffers(T.user2.appUserIdentifier, MANAGER_PUB)
    T.expect(offers).to.have.length(1)
    T.expect(offers[0].label).to.equal("nmanage create")
    T.expect(offers[0].payer_data).to.deep.equal(["order_id"])
    T.expect(offers[0].management_pubkey).to.equal(MANAGER_PUB)
    T.d("handleRequest create persisted managed offer")
}

