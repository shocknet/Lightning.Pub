const recoverEliot = async (T: TestBase, seed: string[], scb: Buffer) => {
    const storageSettings = GetTestStorageSettings()
    const initOk = await initSettings(getLogger({ component: "mainForTest" }), storageSettings)
    if (!initOk) {
        throw new Error("failed to initialize settings manager")
    }
    T.d("recovery settings manager initialized")
    const { settingsManager, unlocker } = initOk
    settingsManager.OverrideTestSettings(s => {
        s.liquiditySettings.disableLiquidityProvider = true
        s.liquiditySettings.liquidityProviderPub = ""
        s.liquiditySettings.useOnlyLiquidityProvider = false
        s.lndNodeSettings = eliot2Settings
        return s
    })
    await unlocker.Unlock({ seed })
    T.d("recovery wallet unlocked")
    await unlocker.ApplyScb(scb)
    T.d("SCB applied")
    const info = await T.main.lnd.GetInfo()
    T.d("Eliot info: " + info.identityPubkey)
    const balance = await T.main.lnd.GetBalance()
    T.d("Balance: " + balance)
    const channels = await T.main.lnd.ListChannels()
    T.d("Channels: " + channels.channels.length + " " + channels.channels.map(c => c.active))
}