import * as Types from '../../proto/autogenerated/ts/types.js'
import { expect, expectThrowsAsync, runSanityCheck, safelySetUserBalance, TestBase } from './testBase.js'
export const ignore = false
export const dev = false

const userContext = (T: TestBase, user: TestBase["user1"]): Types.UserContext => ({
    app_id: T.app.appId,
    app_user_id: user.appUserIdentifier,
    user_id: user.userId,
})

export default async (T: TestBase) => {
    await safelySetUserBalance(T, T.user1, 2000)
    await testSuccessfulInternalAddressPayment(T)
    await testFailedInternalAddressPayment(T)
    await runSanityCheck(T)
}

const testSuccessfulInternalAddressPayment = async (T: TestBase) => {
    T.d("starting testSuccessfulInternalAddressPayment")
    const user2Address = await T.main.paymentManager.NewAddress(userContext(T, T.user2), { addressType: Types.AddressType.WITNESS_PUBKEY_HASH })
    expect(user2Address.address).to.startWith("bcrt1")
    T.d("generated internal chain address for user2")

    const pay = await T.main.paymentManager.PayAddress(userContext(T, T.user1), {
        address: user2Address.address,
        amountSats: 1000,
        satsPerVByte: 1,
    })
    expect(pay.txId).to.not.be.undefined
    expect(pay.network_fee).to.be.equal(0)
    expect(pay.service_fee).to.be.equal(6)
    expect(pay.operation_id).to.startWith(`${Types.UserOperationType.OUTGOING_TX}-`)
    T.d("paid 1000 sats to user2's internal address from user1")

    const application = await T.main.storage.applicationStorage.GetApplication(T.app.appId)
    const u1 = await T.main.storage.userStorage.GetUser(T.user1.userId)
    const u2 = await T.main.storage.userStorage.GetUser(T.user2.userId)
    const owner = await T.main.storage.userStorage.GetUser(application.owner.user_id)
    expect(u2.balance_sats).to.be.equal(1000)
    T.d("user2 balance is 1000")
    expect(u1.balance_sats).to.be.equal(994)
    T.d("user1 balance is 994 cuz he paid 6 sats fee")
    expect(owner.balance_sats).to.be.equal(6)
    T.d("app balance is 6 sats")

    const outgoingTx = await T.main.storage.paymentStorage.GetUserTransactionPaymentOwner(user2Address.address, pay.txId)
    expect(outgoingTx?.internal).to.be.true
    const incomingTx = await T.main.storage.paymentStorage.GetAddressReceivingTransactionOwner(user2Address.address, pay.txId)
    expect(incomingTx?.internal).to.be.true
    T.d("payment is recorded as internal in storage")
}

const testFailedInternalAddressPayment = async (T: TestBase) => {
    T.d("starting testFailedInternalAddressPayment")
    const user2Address = await T.main.paymentManager.NewAddress(userContext(T, T.user2), { addressType: Types.AddressType.WITNESS_PUBKEY_HASH })
    await expectThrowsAsync(T.main.paymentManager.PayAddress(userContext(T, T.user1), {
        address: user2Address.address,
        amountSats: 1000,
        satsPerVByte: 1,
    }), "not enough balance to decrement")
    T.d("payment failed as expected, with the expected error message")
}
