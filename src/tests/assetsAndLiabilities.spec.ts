import * as Types from "../../proto/autogenerated/ts/types.js"
import { defaultInvoiceExpiry } from "../services/storage/paymentStorage.js"
import Main from "../services/main/index.js"
import { runSanityCheck, safelySetUserBalance, TestBase, TestUserData } from "./testBase.js"
import { initBootstrappedInstance } from "./setupBootstrapped.js"

export const ignore = false
export const dev = false

const emptyReq = (): Types.AssetsAndLiabilitiesReq => ({
    lnd_providers: [],
    liquidity_providers: [],
})

const getAssets = (T: TestBase, req = emptyReq()) =>
    T.main.adminManager.GetAssetsAndLiabilities(req)

export default async (T: TestBase) => {
    await testUsersBalance(T)
    await testLndProviderTracked(T)
    await testLndTrackedUserPayment(T)
    await testLndPagination(T)
    await testLiquidityProviderTracked(T)
    await testLiquidityProviderUntrackedOnMain(T)
    await runSanityCheck(T)
}

const testUsersBalance = async (T: TestBase) => {
    T.d("starting testUsersBalance")
    await safelySetUserBalance(T, T.user1, 2000)
    await safelySetUserBalance(T, T.user2, 500)
    const res = await getAssets(T)
    T.expect(Types.AssetsAndLiabilitiesValidate(res)).to.equal(null)
    T.expect(res.users_balance).to.equal(2500)
    T.d("users_balance matches total user balances")
}

const testLndProviderTracked = async (T: TestBase) => {
    T.d("starting testLndProviderTracked")
    const lndInfo = await T.main.lnd.GetInfo()
    const res = await getAssets(T, {
        lnd_providers: [{ pubkey: lndInfo.identityPubkey }],
        liquidity_providers: [],
    })
    const lnd = res.lnds.find(p => p.pubkey === lndInfo.identityPubkey)
    T.expect(lnd).to.not.equal(undefined)
    T.expect(lnd!.tracked).to.not.equal(undefined)
    T.expect(lnd!.tracked!.confirmed_balance).to.be.a("number")
    T.expect(lnd!.tracked!.payments.operations).to.be.an("array")
    T.expect(lnd!.tracked!.invoices.operations).to.be.an("array")
    T.d("lnd provider returns tracked balances and operation pages")
}

const testLndTrackedUserPayment = async (T: TestBase) => {
    T.d("starting testLndTrackedUserPayment")
    const invoice = await T.externalAccessToOtherLnd.NewInvoice(
        500, "assets-liabilities", defaultInvoiceExpiry, { from: "system", useProvider: false }
    )
    await T.main.appUserManager.PayInvoice(
        { app_id: T.user1.appId, user_id: T.user1.userId, app_user_id: T.user1.appUserIdentifier },
        { invoice: invoice.payRequest, amount: 0 }
    )

    const lndInfo = await T.main.lnd.GetInfo()
    const res = await getAssets(T, {
        lnd_providers: [{ pubkey: lndInfo.identityPubkey, limit_payments: 50 }],
        liquidity_providers: [],
    })
    const lnd = res.lnds.find(p => p.pubkey === lndInfo.identityPubkey)
    const userPayment = lnd!.tracked!.payments.operations.find(
        op => op.tracked?.type === Types.TrackedOperationType.USER
            && op.tracked.user_id === T.user1.userId
            && op.amount === 500
    )
    T.expect(userPayment).to.not.equal(undefined)
    T.d("lnd payment is tracked as a user operation")
}

const testLndPagination = async (T: TestBase) => {
    T.d("starting testLndPagination")
    const lndInfo = await T.main.lnd.GetInfo()
    const res = await getAssets(T, {
        lnd_providers: [{ pubkey: lndInfo.identityPubkey, limit_payments: 1 }],
        liquidity_providers: [],
    })
    const payments = res.lnds[0].tracked!.payments
    T.expect(payments.operations.length).to.be.at.most(1)
    if (payments.has_more) {
        T.expect(payments.next_index_offset).to.be.a("number")
    }
    T.d("lnd payment pagination respects limit")
}

const testLiquidityProviderTracked = async (T: TestBase) => {
    T.d("starting testLiquidityProviderTracked")
    const { bootstrapped, bootstrappedUser, stop } = await initBootstrappedInstance(T)
    try {
        await inboundPaymentOnBootstrapped(T, bootstrapped, bootstrappedUser)
        const res = await bootstrapped.adminManager.GetAssetsAndLiabilities({
            lnd_providers: [],
            liquidity_providers: [{ pubkey: T.app.publicKey, limit: 50 }],
        })
        const lp = res.liquidity_providers.find(p => p.pubkey === T.app.publicKey)
        T.expect(lp).to.not.equal(undefined)
        T.expect(lp!.tracked).to.not.equal(undefined)
        T.expect(lp!.tracked!.balance).to.equal(1000)
        const userInvoice = lp!.tracked!.invoices.operations.find(
            op => op.tracked?.type === Types.TrackedOperationType.USER
                && op.tracked.user_id === bootstrappedUser.userId
                && op.amount === 1000
        )
        T.expect(userInvoice).to.not.equal(undefined)
        T.d("liquidity provider returns tracked balance and user invoice")
    } finally {
        stop()
    }
}

const testLiquidityProviderUntrackedOnMain = async (T: TestBase) => {
    T.d("starting testLiquidityProviderUntrackedOnMain")
    const { stop } = await initBootstrappedInstance(T)
    try {
        const res = await getAssets(T, {
            lnd_providers: [],
            liquidity_providers: [{ pubkey: T.app.publicKey, limit: 50 }],
        })
        const lp = res.liquidity_providers.find(p => p.pubkey === T.app.publicKey)
        T.expect(lp).to.not.equal(undefined)
        T.expect(lp!.tracked).to.equal(undefined)
        T.d("main instance does not track remote liquidity provider details")
    } finally {
        stop()
    }
}

const inboundPaymentOnBootstrapped = async (T: TestBase, bootstrapped: Main, user: TestUserData) => {
    const ctx = { app_id: user.appId, user_id: user.userId, app_user_id: user.appUserIdentifier }
    const invoiceRes = await bootstrapped.appUserManager.NewInvoice(ctx, { amountSats: 1000, memo: "assetsLiabilities" })
    await T.externalAccessToOtherLnd.PayInvoice(
        invoiceRes.invoice, 0, { routingFeeLimit: 100, serviceFee: 100 }, 1000,
        { from: "system", useProvider: false }
    )
    await new Promise(resolve => setTimeout(resolve, 200))
}
