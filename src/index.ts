import 'dotenv/config'
import NewServer from '../proto/autogenerated/ts/express_server.js'
import GetServerMethods from './services/serverMethods/index.js'
import serverOptions from './auth.js';
import nostrMiddleware from './nostrMiddleware.js'
import { getLogger } from './services/helpers/logger.js';
import { initMainHandler, initSettings } from './services/main/init.js';
import { nip19 } from 'nostr-tools'
import { LoadStorageSettingsFromEnv } from './services/storage/index.js';
import { AppInfo } from './services/nostr/nostrPool.js';
//@ts-ignore
const { nprofileEncode } = nip19


const start = async () => {
    const log = getLogger({})
    //const mainSettings = LoadMainSettingsFromEnv()
    const storageSettings = LoadStorageSettingsFromEnv()
    /* 
        // BACKUP CHANGE: Check for CLI restore command before normal startup.
        // Restore runs against a clean DB and exits — no need for full init.
        const restored = await handleRestoreCli(log, storageSettings)
        if (restored) {
            log("Restore complete, exiting.")
            process.exit(0)
        } */

    const initOk = await initSettings(log, storageSettings)
    if (!initOk) {
        log("early manual process ended")
        return
    }
    const { settingsManager, restore, backupManager } = initOk
    const keepOn = await initMainHandler(log, settingsManager, restore, backupManager)
    /* if (!keepOn) {
        log("manual process ended")
        return
    } */

    const { mainHandler, localProviderClient, wizard, adminManager } = keepOn
    const serverMethods = GetServerMethods(mainHandler)
    log("initializing nostr middleware")
    const relays = settingsManager.getSettings().nostrRelaySettings.relays
    const maxEventContentLength = settingsManager.getSettings().nostrRelaySettings.maxEventContentLength
    const apps: AppInfo[] = keepOn.apps.map(app => {
        return {
            appId: app.appId,
            privateKey: app.privateKey,
            publicKey: app.publicKey,
            name: app.name,
            provider: app.publicKey === localProviderClient.publicKey ? {
                clientId: `client_${localProviderClient.appId}`,
                pubkey: settingsManager.getSettings().liquiditySettings.liquidityProviderPub,
                relayUrl: settingsManager.getSettings().liquiditySettings.providerRelayUrl
            } : undefined
        }
    })
    const { Send, Stop, Ping, Reset } = nostrMiddleware(serverMethods, mainHandler,
        {
            relays, maxEventContentLength, apps
        },
        (e, p) => mainHandler.liquidityProvider.onEvent(e, p)
    )
    const SHUTDOWN_GRACE_MS = 30_000
    const shutdown = async (exitCode: number) => {
        try {
            Stop()
            await Promise.race([
                mainHandler.gracefulShutdown(),
                new Promise<void>((_, reject) =>
                    setTimeout(() => reject(new Error('shutdown grace period expired')), SHUTDOWN_GRACE_MS)),
            ])
        } catch (err: unknown) {
            const msg = err instanceof Error ? err.message : String(err)
            log(`Shutdown: ${msg}`)
        }
        process.exit(exitCode)
    }
    exitHandler(shutdown, () => {
        Stop()
        mainHandler.Stop()
    })
    log("starting server")
    mainHandler.attachNostrSend(Send)
    mainHandler.attachNostrProcessPing(Ping)
    mainHandler.attachNostrReset(Reset)
    mainHandler.StartBeacons()
    const appNprofile = nprofileEncode({ pubkey: localProviderClient.publicKey, relays })
    if (wizard) {
        wizard.AddConnectInfo(appNprofile, relays)
    }
    adminManager.setAppNprofile(appNprofile)
    const Server = NewServer(serverMethods, serverOptions(mainHandler))
    Server.Listen(settingsManager.getSettings().serviceSettings.servicePort)
}
start()

const exitHandler = (shutdown: (code: number) => Promise<void>, syncKill: () => void) => {
    let shuttingDown = false
    const startShutdown = (code: number, label: string) => {
        if (shuttingDown) {
            return
        }
        shuttingDown = true
        console.log(`${label}, shutting down...`)
        void shutdown(code)
    }

    process.on('SIGINT', () => startShutdown(2, 'SIGINT (Ctrl-C)'))
    process.on('SIGTERM', () => startShutdown(0, 'SIGTERM'))

    process.on('uncaughtException', (e) => {
        console.log('Uncaught Exception detected, exiting safely, and killing all child processes...')
        console.log(e.stack)
        syncKill()
        process.exit(99)
    })
}